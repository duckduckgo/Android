package com.duckduckgo.app.global

import android.content.ContentProvider
import android.content.ContentValues
import android.database.Cursor
import android.net.Uri
import android.os.Debug
import java.text.SimpleDateFormat
import java.util.*

class AppStartListener : ContentProvider() {
    override fun onCreate(): Boolean {
        val fileNameFormat = SimpleDateFormat(
            "yyyy-MM-dd_HH-mm-ss_SSS'.trace'",
            Locale.US
        )
        val fileName = fileNameFormat.format(Date())
        Debug.startMethodTracingSampling(fileName,50*1000*1000,1000)
        return false
    }

    override fun query(uri: Uri, projection: Array<out String>?, selection: String?, selectionArgs: Array<out String>?, sortOrder: String?): Cursor? {
        return null
    }

    override fun getType(uri: Uri): String? {
        return null
    }

    override fun insert(uri: Uri, values: ContentValues?): Uri? {
        return null
    }

    override fun delete(uri: Uri, selection: String?, selectionArgs: Array<out String>?): Int {
        return 0
    }

    override fun update(uri: Uri, values: ContentValues?, selection: String?, selectionArgs: Array<out String>?): Int {
        return 0
    }
}